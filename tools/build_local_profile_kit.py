def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    action = parser.add_mutually_exclusive_group(required=True)
    action.add_argument("--write", action="store_true")
    action.add_argument("--check", action="store_true")
    args = parser.parse_args()

    try:
        expected = render_markdown()
    except ValueError as exc:
        print(f"Local profile kit findings: {exc}")
        return 1

    if args.write:
        OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
        OUTPUT_PATH.write_text(expected, encoding="utf-8")
        print(f"Wrote {OUTPUT_PATH.relative_to(ROOT)}")
        return 0

    if not OUTPUT_PATH.is_file():
        print(f"Local profile kit findings: missing {OUTPUT_PATH.relative_to(ROOT)}")
        return 1
    actual = OUTPUT_PATH.read_text(encoding="utf-8")
    if actual != expected:
        print(f"Local profile kit findings: {OUTPUT_PATH.relative_to(ROOT)} is stale")
        return 1

    print("Local profile launch kit check passed")
    return 0


if __name__ == "__main__":
    sys.exit(main())
