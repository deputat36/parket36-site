def campaign_urls() -> dict[str, str]:
    _, rows = build_rows()
    by_name = {row["name"]: row["url"] for row in rows}
    missing = [name for name in REQUIRED_LINKS.values() if name not in by_name]
    if missing:
        raise ValueError("missing campaign links: " + ", ".join(missing))
    return {key: by_name[name] for key, name in REQUIRED_LINKS.items()}


